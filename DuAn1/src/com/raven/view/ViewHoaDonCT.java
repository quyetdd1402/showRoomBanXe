    public void loadTable(List<HoaDonCT> lst) {
        DefaultTableModel dtm = (DefaultTableModel) tblHoaDonCT.getModel();
        dtm.setRowCount(0);
        for (HoaDonCT hd : lst) {
            Object rowData[] = {
                hd.getMaSP(),
                hd.getTenSP(),
                hd.getDonGia(),
                hd.getSoLuong(),
                hd.getTienThua(),
                hd.getTongTien(),
                hd.getTrangThai() == 1 ? "Da Thanh Toan" : "Chua Thanh Toan"

            };
            dtm.addRow(rowData);
        }
    }

