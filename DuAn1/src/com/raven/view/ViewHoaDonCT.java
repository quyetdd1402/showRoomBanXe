    public void XuatHoaDon(String idhd) {
        try {
            Hashtable map = new Hashtable();
            map.put("maHD1", idhd);//
            JasperReport rpt = JasperCompileManager.compileReport("src/com/raven/view/XuatHoaDonCT.jrxml");
            //map.put("MaHoaDon", cbbMaHoaDonBangHDCT.getSelectedItem());
            java.sql.Connection con = DBConnect.getConnection();
            //  java.sql.Connection connection = null;
            JasperPrint p = JasperFillManager.fillReport(rpt, map, con);
            JasperViewer.viewReport(p, false);
        } catch (JRException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

