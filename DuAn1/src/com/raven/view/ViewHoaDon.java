    public void loadTable(List<HoaDon> lst) {
        DefaultTableModel dtm = (DefaultTableModel) tblHoaDon.getModel();
        dtm.setRowCount(0);
        for (HoaDon hd : lst) {
            Object rowData[] = {
                hd.getMaHD(),
                hd.getTenXe(),
                hd.getTenNV(),
                hd.getTenKH(),
                hd.getNgayTao(),
                hd.getNgayThanhToan(),
                hd.getTongTien(),
                hd.getSoTienGiam(),
                hd.getSdtNguoiNhan(),
                hd.getBaoHiem() == 1 ? "Có" : "Không",
                hd.getLamBien() == 1 ? "Có" : "Không",
                hd.getTrangThai() == 1 ? "Đã Thanh Toán" : "Chưa Thanh Toán"
            
            };
            dtm.addRow(rowData);
        }
    }

