    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        // TODO add your handling code here:

        int row = tblHoaDon.getSelectedRow();
        if (row == -1) {
            return;
        }
        ViewHoaDonCT view = new ViewHoaDonCT(tblHoaDon.getValueAt(row, 0).toString());
        view.setVisible(true);
        String maHD = tblHoaDon.getValueAt(row, 0).toString();
        String tenXe = tblHoaDon.getValueAt(row, 1).toString();
        String tenNV = tblHoaDon.getValueAt(row, 2).toString();
        String tenKH = tblHoaDon.getValueAt(row, 3).toString();
        try {
            tblHoaDon.getSelectedRow();
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tblHoaDon.getValueAt(row, 4).toString());
            txtNgayTao.setDate(date);
        } catch (Exception e) {
            
        }
        try {
            tblHoaDon.getSelectedRow();
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tblHoaDon.getValueAt(row, 5).toString());
            txtNgayThanhToan.setDate(date);
        } catch (Exception e) {
        }
        
        String tongTien = tblHoaDon.getValueAt(row, 6).toString();
        String soTienGiam = tblHoaDon.getValueAt(row, 7).toString();
        String soDienThoai = tblHoaDon.getValueAt(row, 8).toString();
        String trangThai = tblHoaDon.getValueAt(row, 11).toString();
        if (tblHoaDon.getValueAt(row, 9).toString().equalsIgnoreCase("Có")) {
            rdoYesBH.setSelected(true);
        } else {
            rdoNoBH.setSelected(true);
        }
        if (tblHoaDon.getValueAt(row, 10).toString().equalsIgnoreCase("Có")) {
            rdoYesLB.setSelected(true);
        } else {
            rdoNoLB.setSelected(true);
        }
        
        txtMaHD.setText(maHD);
        txtTenXe.setText(tenXe);
        txtTenNV.setText(tenNV);
        txtTenKH.setText(tenKH);
        txtTongTien.setText(tongTien);
        txtSoTienGiam.setText(soTienGiam);
        txtSoDienThoai.setText(soDienThoai);
        
        if (trangThai == "Đã Thanh Toán") {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
        

    }//GEN-LAST:event_tblHoaDonMouseClicked

