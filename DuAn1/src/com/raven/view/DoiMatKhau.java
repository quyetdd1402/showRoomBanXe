    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtName.getText();
        String pass = txtPass.getText();
        String passLai = txtNhapLai.getText();
        if (name.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Tài khoản không được để trống", "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (pass.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Mật khẩu không được để trống", "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (passLai.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập lại mật khẩu không được để trống", "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!pass.equals(passLai)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu không trùng khớp", "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            User user = new User(name, pass);
            JOptionPane.showMessageDialog(this, usimpl.update(user, name));
            ViewLogin v = new ViewLogin();
            v.setVisible(true);
            return;
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

